// Buatkan Program bilangan deret menggunakan while 
// 3 6 9 12 15 18 21 24 27 30

#include <iostream>

using namespace std;

int main()
{
    int i = 1;
    while ( i <= 10 ){
        cout << i << " X 3 = " << i * 3 << endl;
        i++;
    }
    cout << "\n Terima Kasihh" << endl;

    return 0;
}